export function getMenuItemBySlug(slug: string): MenuItem | undefined {
  return menuItems.find((item) => item.slug === slug);
}

export function getMenuItemById(id: string): MenuItem | undefined {
  return menuItems.find((item) => item.id === id);
}

export function getPopularItems(limit = 8): MenuItem[] {
  return menuItems
    .filter((item) => item.isPopular || item.isBestSeller)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}

export function getRelatedItems(item: MenuItem, limit = 4): MenuItem[] {
  const sameCategory = menuItems.filter(
    (candidate) => candidate.category === item.category && candidate.id !== item.id,
  );
  if (sameCategory.length >= limit) {
    return sameCategory.slice(0, limit);
  }

  const fillers = menuItems
    .filter(
      (candidate) =>
        candidate.id !== item.id &&
        !sameCategory.some((s) => s.id === candidate.id) &&
        (candidate.isPopular || candidate.isBestSeller),
    )
    .slice(0, limit - sameCategory.length);

  return [...sameCategory, ...fillers].slice(0, limit);
}
